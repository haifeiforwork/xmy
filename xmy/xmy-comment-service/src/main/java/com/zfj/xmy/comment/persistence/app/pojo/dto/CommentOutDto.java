package com.zfj.xmy.comment.persistence.app.pojo.dto;

import java.util.List;

import com.zfj.xmy.common.persistence.pojo.Comment;

public class CommentOutDto extends Comment {
	/**
	 * 用户名
	 */
	private String userName;
	/**
	 * 评论图片集合
	 */
	private List<String> commentImage;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public List<String> getCommentImage() {
		return commentImage;
	}
	public void setCommentImage(List<String> commentImage) {
		this.commentImage = commentImage;
	}

}
