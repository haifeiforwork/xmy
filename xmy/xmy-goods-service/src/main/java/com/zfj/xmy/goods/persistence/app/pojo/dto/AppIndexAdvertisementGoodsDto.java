package com.zfj.xmy.goods.persistence.app.pojo.dto;

import java.util.List;

import com.zfj.xmy.common.persistence.pojo.app.AppAdvertisementOutDto;
import com.zfj.xmy.common.persistence.pojo.app.AppGoodsCondense;

public class AppIndexAdvertisementGoodsDto {
	//一级分类的id
	private Long categoryId;
	//一级分类的名称
	private String name;
	//广告图片
	private AppAdvertisementOutDto advertisement;
	//剩下的正常商品
	private List<AppGoodsCondense> goods;
	public Long getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(Long categoryId) {
		this.categoryId = categoryId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public AppAdvertisementOutDto getAdvertisement() {
		return advertisement;
	}
	public void setAdvertisement(AppAdvertisementOutDto advertisement) {
		this.advertisement = advertisement;
	}
	public List<AppGoodsCondense> getGoods() {
		return goods;
	}
	public void setGoods(List<AppGoodsCondense> goods) {
		this.goods = goods;
	}
	
}
