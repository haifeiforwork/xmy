package com.zfj.xmy.goods.persistence.app.pojo.dto;

import java.math.BigDecimal;
import java.util.Date;
/**
 * 
 * @author lij
 *
 */
public class AppGoodsDto {
	
	private Long id;
	
	private String fullName;//商品名称
	
	private BigDecimal price;//商品价格
	
	private String imgPath;//商品图片
	
	private Integer sumDeal;//总销售量
	
	private Integer sumComment;//总评论量
	
	private Date publishTime;//上架时间
	
	private String standard;//商品规格

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}

	public String getImgPath() {
		return imgPath;
	}

	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}

	public Integer getSumDeal() {
		return sumDeal;
	}

	public void setSumDeal(Integer sumDeal) {
		this.sumDeal = sumDeal;
	}

	public Integer getSumComment() {
		return sumComment;
	}

	public void setSumComment(Integer sumComment) {
		this.sumComment = sumComment;
	}

	public Date getPublishTime() {
		return publishTime;
	}

	public void setPublishTime(Date publishTime) {
		this.publishTime = publishTime;
	}

	public String getStandard() {
		return standard;
	}

	public void setStandard(String standard) {
		this.standard = standard;
	}
	
}
