public class PcContainerDto {
	
	private String name; 
	
	private String slogan; //货柜口号
	
	private String fontColor ;
	
	private AdImage adImage; //货柜商品广告
	
	private List<PcGoodsDir> goodsList = new ArrayList<PcGoodsDir>();

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSlogan() {
		return slogan;
	}

	public void setSlogan(String slogan) {
		this.slogan = slogan;
	}

	public String getFontColor() {
		return fontColor;
	}

	public void setFontColor(String fontColor) {
		this.fontColor = fontColor;
	}

	public AdImage getAdImage() {
		return adImage;
	}

	public void setAdImage(AdImage adImage) {
		this.adImage = adImage;
	}

	public List<PcGoodsDir> getGoodsList() {
		return goodsList;
	}

	public void setGoodsList(List<PcGoodsDir> goodsList) {
		this.goodsList = goodsList;
	}
	
	
	
	
	
}
  