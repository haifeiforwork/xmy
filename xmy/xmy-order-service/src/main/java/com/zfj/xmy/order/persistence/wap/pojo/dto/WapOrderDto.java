package com.zfj.xmy.order.persistence.wap.pojo.dto;

import com.zfj.xmy.common.persistence.pojo.Order;

/**
 * wap端OrderDto
 * @author cj
 * @createDate 2017年10月26日
 *
 */
public class WapOrderDto extends Order{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	//多个商品拼接的字符串
	private String goodsId;
	
	//收货地址ID
	private Long addressId;
	
	//身份证姓名
	private String idCardName;
	
	//配送时间字符串
	private String postTime;
	
	//是否含有跨境商品
	private Integer isCrossGoods;
	
	public String getGoodsId() {
		return goodsId;
	}

	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}

	public Long getAddressId() {
		return addressId;
	}

	public void setAddressId(Long addressId) {
		this.addressId = addressId;
	}

	public Integer getIsCrossGoods() {
		return isCrossGoods;
	}

	public void setIsCrossGoods(Integer isCrossGoods) {
		this.isCrossGoods = isCrossGoods;
	}

	public String getIdCardName() {
		return idCardName;
	}

	public void setIdCardName(String idCardName) {
		this.idCardName = idCardName;
	}

	public String getPostTime() {
		return postTime;
	}

	public void setPostTime(String postTime) {
		this.postTime = postTime;
	}
	
}
