package com.zfj.xmy.order.persistence.cms.pojo.dto;

import java.math.BigDecimal;
import java.util.Date;

/**
 * 订单总表导出实体类
 * @author Administrator
 *
 */
public class OrderExcleDto {
	private String no;//订单编号
	private Date caretDate;//订单创建时间
	private BigDecimal total;//总金额(不含优惠卷)
	private BigDecimal pay;//购物卡支付金额
	private BigDecimal autal;//第三方实际支付
	private BigDecimal couponMoney;//优惠卷金额
	private Integer payType;//订单支付方式
	private Integer trable;//实际支付方式
	private String remark;//订单备注
	private Long couponId;//优惠卷id
	
	public Long getCouponId() {
		return couponId;
	}
	public void setCouponId(Long couponId) {
		this.couponId = couponId;
	}
	public Integer getPayType() {
		return payType;
	}
	public void setPayType(Integer payType) {
		this.payType = payType;
	}
	public Integer getTrable() {
		return trable;
	}
	public void setTrable(Integer trable) {
		this.trable = trable;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public Date getCaretDate() {
		return caretDate;
	}
	public void setCaretDate(Date caretDate) {
		this.caretDate = caretDate;
	}
	public BigDecimal getTotal() {
		return total;
	}
	public void setTotal(BigDecimal total) {
		this.total = total;
	}
	public BigDecimal getPay() {
		return pay;
	}
	public void setPay(BigDecimal pay) {
		this.pay = pay;
	}
	public BigDecimal getAutal() {
		return autal;
	}
	public void setAutal(BigDecimal autal) {
		this.autal = autal;
	}
	public BigDecimal getCouponMoney() {
		return couponMoney;
	}
	public void setCouponMoney(BigDecimal couponMoney) {
		this.couponMoney = couponMoney;
	}
	
}
